from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any

import pandas as pd


RUN_ID = "GAPREV-MINI-PANEL-PROBE-001"
TRIAL_ID = "TRIAL-GAPREV-001"
INPUT_RESULTS = Path("experiments/provider_aware_research/execution_outputs/GAPREV-MINI-PANEL-PROBE-001/mini_panel_results.csv")
OUTPUT_DIR = Path("experiments/provider_aware_research/gaprev_failure_taxonomy_20260521")
VAULT_REPORT = Path("vault/04-Documentazione/Reports/Report-GapRev-Failure-Taxonomy-2026-05-21.md")
VAULT_DEVLOG = Path("vault/02-Devlog/2026-05/2026-05-21-codex-gaprev-failure-taxonomy.md")


def build_gaprev_failure_taxonomy(results_path: str | Path = INPUT_RESULTS, output_dir: str | Path = OUTPUT_DIR) -> dict[str, Any]:
    results = pd.read_csv(results_path)
    classified = classify_gaprev_failures(results)
    output = Path(output_dir)
    output.mkdir(parents=True, exist_ok=True)
    classified_path = output / "gaprev_failure_taxonomy.csv"
    classified.to_csv(classified_path, index=False)
    summary = summarize_taxonomy(classified)
    _write_json(output / "gaprev_failure_taxonomy_summary.json", summary)
    _write_markdown_report(summary, classified)
    return summary


def classify_gaprev_failures(results: pd.DataFrame) -> pd.DataFrame:
    frame = results.copy()
    categories = []
    for _, row in frame.iterrows():
        trade_count = int(row.get("trade_count", 0))
        reason = str(row.get("reason", "") or "")
        rth_gap = _to_float(row.get("rth_gap_return"))
        rel_vol = _to_float(row.get("relative_opening_volume"))
        gross = _to_float(row.get("gross_return"))
        net = _to_float(row.get("net_return"))
        if trade_count == 0:
            if reason == "setup_filter_not_met" and (rth_gap is None or rth_gap > -0.05):
                category = "RTH_SETUP_FALSE_POSITIVE_FROM_DAILY_GAP"
                interpretation = "Daily gap candidate did not remain a qualifying RTH gap-down after intraday reconstruction."
            elif reason == "setup_filter_not_met" and (rel_vol is None or rel_vol < 2.0):
                category = "RTH_VOLUME_FILTER_FAIL"
                interpretation = "RTH relative opening volume failed the frozen threshold."
            else:
                category = "NO_TRADE_OTHER_FILTER_FAIL"
                interpretation = "No trade generated by frozen setup or VWAP reclaim filters."
        elif gross is not None and gross > 0 and net is not None and net <= 0:
            category = "GROSS_EDGE_COST_DESTROYED"
            interpretation = "Trade had positive gross reversion but failed the mandatory 500 bps cost stress."
        elif net is not None and net > 0:
            category = "NET_WIN_UNPROMOTABLE_SINGLE_EVENT"
            interpretation = "Trade survived costs, but single-event evidence is not promotable."
        elif gross is not None and gross <= 0:
            category = "ADVERSE_CONTINUATION_OR_WEAK_REVERSION"
            interpretation = "Trade failed before or after costs; gap-down did not deliver enough intraday reversion."
        else:
            category = "UNCLASSIFIED"
            interpretation = "Insufficient fields for deterministic classification."
        categories.append((category, interpretation))
    frame["failure_category"] = [category for category, _ in categories]
    frame["failure_interpretation"] = [interpretation for _, interpretation in categories]
    return frame


def summarize_taxonomy(classified: pd.DataFrame) -> dict[str, Any]:
    counts = classified["failure_category"].value_counts().sort_index().to_dict()
    trade_rows = classified[classified["trade_count"].astype(int) > 0].copy()
    no_trade_rows = classified[classified["trade_count"].astype(int) == 0].copy()
    net_returns = pd.to_numeric(trade_rows.get("net_return", pd.Series(dtype=float)), errors="coerce").dropna()
    gross_returns = pd.to_numeric(trade_rows.get("gross_return", pd.Series(dtype=float)), errors="coerce").dropna()
    return {
        "status": "pass",
        "trial_id": TRIAL_ID,
        "run_id": RUN_ID,
        "events_classified": int(len(classified)),
        "trade_count": int(len(trade_rows)),
        "no_trade_count": int(len(no_trade_rows)),
        "category_counts": {str(key): int(value) for key, value in counts.items()},
        "gross_return_sum": float(gross_returns.sum()) if len(gross_returns) else 0.0,
        "net_return_sum": float(net_returns.sum()) if len(net_returns) else 0.0,
        "positive_net_trade_count": int((net_returns > 0).sum()) if len(net_returns) else 0,
        "primary_failure_mode": _primary_failure_mode(counts),
        "decision": "GAPREV_ARCHIVE_CURRENT_FORM",
        "no_new_query_performed": True,
        "strategy_promotion_allowed": False,
    }


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Build GAPREV mini-panel failure taxonomy.")
    parser.add_argument("--results", default=str(INPUT_RESULTS))
    parser.add_argument("--output-dir", default=str(OUTPUT_DIR))
    args = parser.parse_args(argv)
    report = build_gaprev_failure_taxonomy(args.results, args.output_dir)
    print(json.dumps(report, indent=2, sort_keys=True))
    return 0 if report["status"] == "pass" else 1


def _primary_failure_mode(counts: dict[str, int]) -> str:
    if not counts:
        return "none"
    return max(counts.items(), key=lambda item: item[1])[0]


def _write_markdown_report(summary: dict[str, Any], classified: pd.DataFrame) -> None:
    VAULT_REPORT.parent.mkdir(parents=True, exist_ok=True)
    VAULT_DEVLOG.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Report GAPREV Failure Taxonomy - 2026-05-21",
        "",
        f"Decision: {summary['decision']}",
        "",
        "## Summary",
        f"- Events classified: {summary['events_classified']}",
        f"- Trades generated: {summary['trade_count']}",
        f"- No-trade events: {summary['no_trade_count']}",
        f"- Gross return sum on traded events: {summary['gross_return_sum']}",
        f"- Net return sum after 500 bps: {summary['net_return_sum']}",
        f"- Positive net trades: {summary['positive_net_trade_count']}",
        f"- Primary failure mode: {summary['primary_failure_mode']}",
        "",
        "## Category Counts",
    ]
    for category, count in summary["category_counts"].items():
        lines.append(f"- {category}: {count}")
    lines.extend(["", "## Interpretation", _interpret_summary(summary), "", "## Event Table"])
    for _, row in classified.iterrows():
        lines.append(
            f"- {row['symbol']} {row['event_date']}: {row['failure_category']} "
            f"(trade_count={row['trade_count']}, net_return={row.get('net_return', '')})"
        )
    text = "\n".join(lines) + "\n"
    VAULT_REPORT.write_text(text, encoding="utf-8")
    VAULT_DEVLOG.write_text(text, encoding="utf-8")


def _interpret_summary(summary: dict[str, Any]) -> str:
    return (
        "GapRev in its current form is archived. Most candidates selected from daily gaps do not remain valid "
        "RTH GapRev trades after intraday reconstruction, and the traded subset is net negative under the "
        "mandatory 500 bps stress cost. This is a structural failure, not a parameter-tuning invitation."
    )


def _write_json(path: Path, payload: dict[str, Any]) -> None:
    path.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")


def _to_float(value: Any) -> float | None:
    try:
        if pd.isna(value):
            return None
        return float(value)
    except (TypeError, ValueError):
        return None


if __name__ == "__main__":
    raise SystemExit(main())
